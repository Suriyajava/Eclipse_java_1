package java_tamil_programming;

public class increament_decreament {
  public static void main(String[] args) {
	
	  int a=10;
	  System.out.println(++a);
	  System.out.println(a);
	  System.out.println(a++);
	  System.out.println(a);
	  System.out.println("decreament values");
	   
	  System.out.println(--a);
	  System.out.println(a);
	  System.out.println(a--);
	  System.out.println(a);
}
}
