package java_tamil_programming;

public class Logical {
	public static void main(String[] args) {
		int a=10,b=15;
		
	  System.out.println(a<b &a<b);
	  System.out.println(a>b||a<b);
	  System.out.println(!(a<b));
	  System.out.println(a!=b);
	  System.out.println(a==b);
	  
		
	}

}
